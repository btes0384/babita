package practise;

public class Pattern5 {

	public static void main(String[] args)
	{
		for(int i=1;i<=7;i++)
		{
			for(int j=1;j<=i;i++)
		
		{
		System.out.print(" ");
		}
			{
		for(int k=i;k<=7;k++)
			
			{
				System.out.print(k+" " );
			}
			System.out.println();

	}
	}


	}
}
